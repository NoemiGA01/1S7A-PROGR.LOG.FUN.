buscar_y_mostrar_plantas(Enfermedad) :-
    buscar_planta_por_enfermedad(Enfermedad, Plantas, Preparaciones),
    mostrar_resultados_con_preparacion('Plantas para tratar', Enfermedad, Plantas, Preparaciones).

% Mostrar resultados con preparación
mostrar_resultados_con_preparacion(Titulo, Consulta, Plantas, Preparaciones) :-
    new(D, dialog(Titulo)),
    send(D, append, text(Consulta)),
    new(LB, list_browser),
    send(LB, width, 50),  % Ajustar el ancho del list_browser según sea necesario
    send(LB, height, 10), % Ajustar el alto del list_browser según sea necesario
    send(D, append, LB),
    mostrar_lista_con_preparacion(Plantas, Preparaciones, LB),
    send(D, open).

mostrar_lista([], _).
mostrar_lista([H|T], LB) :-
    send(LB, append, dict_item(H)),
    mostrar_lista(T, LB).

mostrar_lista_con_preparacion([], [], _).
mostrar_lista_con_preparacion([H|T], [P|PT], LB) :-
    send(LB, append, dict_item(H)),
    send(LB, append, dict_item('Preparacion:')),
    send(LB, append, dict_item(P)),
    mostrar_lista_con_preparacion(T, PT, LB).

% Mostrar efectos y sus descripciones
mostrar_sig_efectos :-
