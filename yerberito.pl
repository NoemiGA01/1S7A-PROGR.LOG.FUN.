    send(D, open).

mostrar_lista_con_descripcion([], _).
mostrar_lista_con_descripcion([E-D|T], Dlg) :-
    send(Dlg, append, text(E)),
    send(Dlg, append, text(D)),
    mostrar_lista_con_descripcion(T, Dlg).

