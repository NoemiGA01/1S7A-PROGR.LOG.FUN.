% Buscar enfermedades que una planta especifica trata y obtener su imagen
buscar_enfermedad_por_planta_con_imagen(Planta, Enfermedades, Imagen) :-
    findall(Enfermedad, planta_enfermedad_imagen_preparacion(Planta, Enfermedad, _, _), Enfermedades),
    planta_enfermedad_imagen_preparacion(Planta, _, Imagen, _).

% Buscar plantas que tratan una enfermedad especifica
buscar_planta_por_enfermedad(Enfermedad, Plantas, Preparaciones) :-
    findall(Planta, planta_enfermedad_imagen_preparacion(Planta, Enfermedad, _, _), Plantas),
    findall(Preparacion, planta_enfermedad_imagen_preparacion(_, Enfermedad, _, Preparacion), Preparaciones).

% Buscar efectos y su descripcion
buscar_efectos(Efectos) :-
    findall(Efecto-Descripcion, efecto(Efecto, Descripcion), Efectos).

% Interfaz grafica para buscar y mostrar los resultados
:- pce_global(@busqueda_prompter, make_busqueda_prompter).

make_busqueda_prompter(P) :-
    new(P, dialog('Consulta de Herbolaria')),
    send(P, append, new(B, bitmap('C:/Proyecto/dragonball/yerberitoIlustrado.jpg'))),  % Anadir imagen de bienvenida
    send(P, display, B, point(100, 10)),  % Ajusta la posicion segun necesites
    send(P, append, button('Botiquin de Plantas', message(@prolog, mostrar_botiquin))),
    send(P, append, button('Nombre Cientifico', message(@prolog, mostrar_nombres_cientificos))),
    send(P, append, button('Plantas Analgesicas', message(@prolog, mostrar_plantas_analgesicas))),
    send(P, append, new(TI, text_item('Ingrese planta o enfermedad'))),
    send(P, append, button('Buscar por planta', message(@prolog, buscar_y_mostrar_enfermedades, TI?selection))),
    send(P, append, button('Buscar por enfermedad', message(@prolog, buscar_y_mostrar_plantas, TI?selection))),
    send(P, append, button('Efectos', message(@prolog, mostrar_efectos))),
    send(P, append, button('Cancelar', message(P, destroy))).

% Funcion para buscar y mostrar enfermedades con imagen
buscar_y_mostrar_enfermedades(Planta) :-
    buscar_enfermedad_por_planta_con_imagen(Planta, Enfermedades, Imagen),
    mostrar_resultados_con_imagen('Enfermedades tratadas por', Planta, Enfermedades, Imagen).

% Mostrar resultados con imagen
mostrar_resultados_con_imagen(Titulo, Planta, Enfermedades, ImagenPath) :-
    new(D, dialog(Titulo)),
    send(D, append, new(B, bitmap(ImagenPath))),
    send(D, display, B, point(100, 20)),
    send(D, append, text('Planta:')),
    send(D, append, text(Planta)),
    send(D, append, text('Sirve para:')),
    mostrar_lista(Enfermedades, D),
    send(D, open).

buscar_y_mostrar_plantas(Enfermedad) :-
    buscar_planta_por_enfermedad(Enfermedad, Plantas, Preparaciones),
    mostrar_resultados_con_preparacion('Plantas para tratar', Enfermedad, Plantas, Preparaciones).

% Mostrar resultados con preparacion
mostrar_resultados_con_preparacion(Titulo, Consulta, Plantas, Preparaciones) :-
    new(D, dialog(Titulo)),
    send(D, append, text(Consulta)),
    mostrar_lista_con_preparacion(Plantas, Preparaciones, D),
    send(D, open).

mostrar_lista([], _).
mostrar_lista([H|T], D) :-
    send(D, append, text(H)),
    mostrar_lista(T, D).

mostrar_lista_con_preparacion([], [], _).
mostrar_lista_con_preparacion([H|T], [P|PT], D) :-
    send(D, append, text(H)),
    send(D, append, text('Preparacion:')),
    send(D, append, text(P)),
    mostrar_lista_con_preparacion(T, PT, D).

% Mostrar efectos y sus descripciones
mostrar_efectos :-
    buscar_efectos(Efectos),
    new(D, dialog('Efectos de las Plantas')),
    send(D, append, text('Lista de Efectos')),
